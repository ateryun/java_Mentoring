package sourceCode_2;

public class Array7 {
    public static void main(String[] args) {
        double arr[] = new double[4];

        arr[0] = 1.1;
        arr[1] = 2.2;
        arr[2] = 3.3;
        arr[3] = 4.4;

        System.out.printf("배열 크기: %d\n", arr.length);
        System.out.printf("arr[0] = %.1f\n", arr[0]);
        System.out.printf("arr[1] = %.1f\n", arr[1]);
        System.out.printf("arr[2] = %.1f\n", arr[2]);
        System.out.printf("arr[3] = %.1f\n", arr[3]);
    }
}
